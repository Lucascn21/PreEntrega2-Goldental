import * as React from "react";
import AppBar from "@mui/material/AppBar";
import Box from "@mui/material/Box";
import Toolbar from "@mui/material/Toolbar";
import IconButton from "@mui/material/IconButton";
import Typography from "@mui/material/Typography";
import Menu from "@mui/material/Menu";
import MenuIcon from "@mui/icons-material/Menu";
import Container from "@mui/material/Container";
import Button from "@mui/material/Button";
import MenuItem from "@mui/material/MenuItem";
import AdbIcon from "@mui/icons-material/Adb";
import { Link as RouterLink } from "react-router-dom";
import { pages } from "../../data/pages";
import { CartWidget } from "./CartWidget";
import { aboutUsPages } from "../../data/aboutUsPages";
import { AboutUsMenu } from "./AboutUsMenu";
import { ColorModeWidget } from "./ColorModeWidget";

export const NavBar = React.memo(() => {
  const [anchorElNav, setAnchorElNav] = React.useState(null);

  const handleOpenNavMenu = (event) => {
    setAnchorElNav(event.currentTarget);
  };

  const handleCloseNavMenu = () => {
    setAnchorElNav(null);
  };

  return (
    <AppBar position="static">
      <Container maxWidth="xl">
        <Toolbar disableGutters>
          <AdbIcon sx={{ display: { xs: "none", md: "flex" }, mr: 1 }} />
          <Typography
            variant="h6"
            noWrap
            component={RouterLink}
            to="/"
            sx={{
              mr: 2,
              display: { xs: "none", md: "flex" },
              fontFamily: "monospace",
              fontWeight: 700,
              letterSpacing: ".3rem",
              color: "inherit",
              textDecoration: "none",
            }}
          >
            AnotherBookstore
          </Typography>

          <Box
            sx={{
              color: "text.primary",
              flexGrow: 1,
              display: { xs: "flex", md: "none" },
            }}
          >
            <IconButton
              size="large"
              aria-label="nav menu"
              aria-controls="menu-appbar"
              aria-haspopup="true"
              onClick={handleOpenNavMenu}
              color="inherit"
            >
              <MenuIcon />
            </IconButton>
            <Menu
              id="menu-appbar"
              anchorEl={anchorElNav}
              anchorOrigin={{
                vertical: "bottom",
                horizontal: "left",
              }}
              keepMounted
              transformOrigin={{
                vertical: "top",
                horizontal: "left",
              }}
              open={Boolean(anchorElNav)}
              onClose={handleCloseNavMenu}
              sx={{
                display: { xs: "block", md: "none" },
              }}
            >
              {pages.map((menuItem) => (
                <MenuItem key={menuItem} onClick={handleCloseNavMenu}>
                  <Button
                    key={menuItem}
                    component={RouterLink}
                    to={`category/${menuItem}`}
                    onClick={handleCloseNavMenu}
                    sx={{ my: 0.25, color: "white", display: "block" }}
                  >
                    {menuItem.replace(/-/g, " ")}
                  </Button>
                </MenuItem>
              ))}
              {aboutUsPages.map((menuItem) => (
                <MenuItem key={menuItem} onClick={handleCloseNavMenu}>
                  <Button
                    key={menuItem}
                    component={RouterLink}
                    to={menuItem}
                    onClick={handleCloseNavMenu}
                    sx={{ my: 0.25, color: "white", display: "block" }}
                  >
                    {menuItem}
                  </Button>
                </MenuItem>
              ))}
            </Menu>
          </Box>
          <AdbIcon sx={{ display: { xs: "flex", md: "none" }, mr: 1 }} />
          <Typography
            variant="h5"
            noWrap
            component={RouterLink}
            to="/"
            sx={{
              mr: 2,
              display: { xs: "flex", md: "none" },
              flexGrow: 1,
              fontFamily: "monospace",
              fontWeight: 700,
              letterSpacing: ".3rem",
              color: "inherit",
              textDecoration: "none",
            }}
          >
            ABookstore
          </Typography>
          <Box sx={{ flexGrow: 1, display: { xs: "none", md: "flex" } }}>
            {pages.map((page) => (
              <Button
                key={page}
                component={RouterLink}
                to={`category/${page}`}
                onClick={handleCloseNavMenu}
                sx={{ my: 2, color: "white", display: "block" }}
              >
                {page.replace(/-/g, " ")}
              </Button>
            ))}
            <AboutUsMenu></AboutUsMenu>
          </Box>
          <ColorModeWidget></ColorModeWidget>
          <CartWidget cartValue={5} />
        </Toolbar>
      </Container>
    </AppBar>
  );
});
